void main() {
  // Data Types

  // Strings
  String firstName = "John";
  print("String: $firstName");

  // Integers
  int myNum = 41;
  print("Integer: $myNum");

  //doubles
  double otherNum = 19.23;
  print("Double: $otherNum");

  //booleans
  bool myBool = true;
  print("Boolean: $myBool");

  //Dynamic
  dynamic myDyn = "John Elder";
  print("Dynamic: $myDyn");
}
